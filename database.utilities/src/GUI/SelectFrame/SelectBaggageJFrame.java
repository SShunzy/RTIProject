    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            ResultSet rs;
            int Id;
            if(IdTF.getText().isEmpty())
                Id =0;
            else
                Id = Integer.parseInt(IdTF.getText());
            
            if(Insert)
            {

                if(Id == 0)
                    System.out.println("L'id n'est pas valide!!");
                else
                {
                    ResultSet rsCompte = BB.Select(true,Id,"");
                    int Compte = 0;
                    while(rsCompte.next())
                        Compte = rsCompte.getInt("total");
                    System.out.println("Compte = "+Compte);
                    if(Compte > 0)
                        System.out.println("L'id n'est pas valide!!");
                    else
                    {
                        rs = BB.Select();
                        rs.moveToInsertRow();
                        rs.updateInt(1,Id);
                        rs.updateString(2,BilletTF.getText());
                        rs.insertRow();
                        BJF.RefreshTable(rs);
                    }
                }
                this.dispose();                  
            }
            else
            {

                rs = BB.Select(false, Id,BilletTF.getText());
                BJF.RefreshTable(rs);
                this.dispose();
                // TODO add your handling code here:
            }
        } catch (SQLException ex) {
            Logger.getLogger(SelectBaggageJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

